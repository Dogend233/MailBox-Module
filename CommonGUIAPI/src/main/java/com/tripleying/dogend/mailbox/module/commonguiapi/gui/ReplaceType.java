package com.tripleying.dogend.mailbox.module.commonguiapi.gui;

/**
 * 属性值类型枚举
 * @author Dogend
 */
public enum ReplaceType {
    
    /**
     * 整型 应为int
     */
    Integer,
    /**
     * 浮点型 应为double
     */
    Double,
    /**
     * 布尔值 应为boolean
     */
    Boolean,
    /**
     * 字符串 应为String
     */
    String,
    /**
     * 日期 应为String
     */
    DateTime,
    /**
     * 字符串列表 应为List[String]
     */
    StringList,
    /**
     * 物品列表 应为List[ItemStack]
     */
    ItemStackList
    
}
